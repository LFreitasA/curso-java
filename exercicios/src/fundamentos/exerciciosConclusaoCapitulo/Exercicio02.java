public class Exercicio02 {
	public static void main(String[] args) {
		double fahrenheit = 55;
		double celsius = (fahrenheit - 32) * 5.0 / 9;
		System.out.printf("A temperatura %f fahrenheit fica %f celsius",fahrenheit ,celsius);
	}
}
